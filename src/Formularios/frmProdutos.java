private void mostrarRegistro(){
    
        txtIDProduto.setText(clsdados.getProdutos()[produtoatual].getIdProduto());
        txtDescricao.setText(clsdados.getProdutos()[produtoatual].getDescricao());
        txtAnotacao.setText(clsdados.getProdutos()[produtoatual].getAnotacao());
        txtPreco.setText("" + clsdados.getProdutos()[produtoatual].getPreco());
        cmdImposto.setSelectedIndex(clsdados.getProdutos()[produtoatual].getImposto());
}
        private void preencherTabela(){
            String titulos[] = {"Espec/Consul","Descrição","Preço","Imposto","Horário"};
            String registro[] = new String[6];
            mTabela = new DefaultTableModel(null,titulos);
            for(int i=0; i<clsdados.numeroProdutos();i++){
                registro[0] = clsdados.getProdutos()[i].getIdProduto();
                registro[1] = clsdados.getProdutos()[i].getDescricao();
                registro[2] = "" + clsdados.getProdutos()[i].getPreco();
                registro[3] = " "+ imposto(clsdados.getProdutos()[i].getImposto());
                registro[4] = clsdados.getProdutos()[i].getAnotacao();
                mTabela.addRow(registro);
                
            }
            tblTabela.setModel(mTabela);
        } 
        private String imposto(int idImposto){
            switch(idImposto){
                case 0: return"%0";
                case 1: return"10%";
                case 2: return"15%";
                default: return"Não definido";
            }
        }
