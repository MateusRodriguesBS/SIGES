    private void cmdsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdsalvarActionPerformed
        // TODO add your handling code here:
        
        if(txtIDProduto.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor, digitar a especialidade para realizar o cadastro da consulta!");
            txtIDProduto.requestFocusInWindow();
            return;
            
        } if(cmdImposto.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(rootPane, "Por favor, selecionar o valor do imposto!");
            cmdImposto.requestFocusInWindow();
            return;
            
        }
        if(txtPreco.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor, digitar o preço!");
            txtPreco.requestFocusInWindow();
            return; 
        }
        if(!Utilidades.isNumeric(txtPreco.getText())){
            JOptionPane.showMessageDialog(rootPane, "Por favor, digitar apenas números!");
            txtPreco.requestFocusInWindow();
            return; 
        }
        int preco = Integer.parseInt(txtPreco.getText());
        if(preco <= 0){
             JOptionPane.showMessageDialog(rootPane, "Por favor, digitar um número acima de zero!");
        }
        if(txtDescricao.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor, uma descrição!");
            txtDescricao.requestFocusInWindow();
            return;      
        }
        
        int poslinha = clsdados.posicaoProduto(txtIDProduto.getText());
        if(cmdNovo){
            if (poslinha != -1){
                JOptionPane.showMessageDialog(rootPane, "Esta especialidade já está cadastrada");
                txtIDProduto.requestFocusInWindow();
                return;
            }
        }else{
            if(poslinha == -1){
                JOptionPane.showMessageDialog(rootPane, "Esta especialidade  não está cadastrada");
                txtIDProduto.requestFocusInWindow();
                return;
            }
        }
        
       Produto mProduto = new Produto(txtIDProduto.getText()
              ,txtDescricao.getText(), 
              preco,
              cmdImposto.getSelectedIndex(),
              txtAnotacao.getText());
       
       String msg;
       if(cmdNovo){
         msg = clsdados.adicionarProduto(mProduto);
       }else{ 
       msg = clsdados.editarProduto(mProduto, poslinha);
       }
       JOptionPane.showMessageDialog(rootPane, msg);
        
        
        cmdprimeiro.setEnabled(true);
        cmdanterior.setEnabled(true);
        cmdproximo.setEnabled(true);
        cmdultimo.setEnabled(true);
        cmdnovo.setEnabled(true);
        cmdeditar.setEnabled(true);
        cmdsalvar.setEnabled(false);
        cmddeletar.setEnabled(true);
        cmdpesquisar.setEnabled(true);
        cmdcancelar.setEnabled(false);
        
        txtIDProduto.setEnabled(false);
        txtPreco.setEnabled(false);
        txtDescricao.setEnabled(false);
        cmdImposto.setEnabled(false);
        txtAnotacao.setEnabled(false);
        txtDescricao.requestFocusInWindow();
        
        preencherTabela();
    }//GEN-LAST:event_cmdsalvarActionPerformed

