private void mostrarRegistro(){
    
        txtcodusuario.setText(clsdados.getUsuarios()[usuarioatual].getCodusuario());
        txtnome.setText(clsdados.getUsuarios()[usuarioatual].getNome());
        txtsenha.setText(clsdados.getUsuarios()[usuarioatual].getSenha());
        txtconfsenha.setText(clsdados.getUsuarios()[usuarioatual].getSenha());
        cmdperfil.setSelectedIndex(clsdados.getUsuarios()[usuarioatual].getPerfil());
}
        private void preencherTabela(){
            String titulos[] = {"CodUsuário","Nome","Perfil"};
            String registro[] = new String[3];
            mTabela = new DefaultTableModel(null,titulos);
            for(int i=0; i<clsdados.numeroUsuarios(); i++){
                registro[0] = clsdados.getUsuarios()[i].getCodusuario();
                registro[1] = clsdados.getUsuarios()[i].getNome();
                registro[2] = perfil(+ clsdados.getUsuarios()[i].getPerfil());
                mTabela.addRow(registro);
                
            }
            tblTabela.setModel(mTabela);
        } 
        private String perfil(int idperfil){
            if(idperfil == 1){
                return "Administrador";
            }else{
                 return "Funcionário";
            }
        }
