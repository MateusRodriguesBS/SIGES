    private void cmdsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdsalvarActionPerformed
        // TODO add your handling code here:
        
        if(txtcodusuario.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor, digitar o seu código para realizar seu cadastro!");
            txtcodusuario.requestFocusInWindow();
            return;
            
        } if(cmdperfil.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(rootPane, "Por favor, selecionar um perfil para realizar seu cadastro!");
            cmdperfil.requestFocusInWindow();
            return;
            
        } if(txtnome.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor, digitar o seu nome para realizar seu cadastro!");
            txtnome.requestFocusInWindow();
            return;      
        }
        String sSenha = new String (txtsenha.getPassword());
        String confSenha = new String(txtconfsenha.getPassword());
        
        if(sSenha.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor, digitar a sua senha para realizar seu cadastro!");
            txtsenha.requestFocusInWindow();
            return;
            
        } if (confSenha.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor, confirmar a para realizar seu cadastro!");
            txtsenha.setText("");
            txtconfsenha.requestFocusInWindow();
            return;
        }
        if (!sSenha.equals(confSenha)){
            JOptionPane.showMessageDialog(rootPane, "Confirmação de senha incorreta!");
            txtsenha.setText("");
            txtconfsenha.setText("");
            txtsenha.requestFocusInWindow();
            return;
        }
        
        int poslinha = clsdados.LinhaUsuario(txtcodusuario.getText());
        if(cmdNovo){
            if (poslinha != -1){
                JOptionPane.showMessageDialog(rootPane, "Este usuário já está cadastrado");
                txtcodusuario.requestFocusInWindow();
                return;
            }
        }else{
            if(poslinha == -1){
                JOptionPane.showMessageDialog(rootPane, "Este usuário não está cadastrado");
                txtcodusuario.requestFocusInWindow();
                return;
            }
        }
        
       Usuarios Musuario = new Usuarios(txtcodusuario.getText()
               ,txtnome.getText(), 
               sSenha, 
               cmdperfil.getSelectedIndex());
       
       String msg;
       if(cmdNovo){
         msg = clsdados.CadUsuarios(Musuario);
       }else{ 
       msg = clsdados.editar(Musuario, poslinha);   
       }
       JOptionPane.showMessageDialog(rootPane, msg);
        
        
        cmdprimeiro.setEnabled(true);
        cmdanterior.setEnabled(true);
        cmdproximo.setEnabled(true);
        cmdultimo.setEnabled(true);
        cmdnovo.setEnabled(true);
        cmdeditar.setEnabled(true);
        cmdsalvar.setEnabled(false);
        cmddeletar.setEnabled(true);
        cmdpesquisar.setEnabled(true);
        cmdcancelar.setEnabled(false);
        
        txtcodusuario.setEnabled(false);
        txtnome.setEnabled(false);
        txtsenha.setEnabled(false);
        txtconfsenha.setEnabled(false);
        cmdperfil.setEnabled(false);
        
        txtnome.requestFocusInWindow();
        
        preencherTabela();
        
        
        

    }//GEN-LAST:event_cmdsalvarActionPerformed

    private void cmddeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmddeletarActionPerformed
        // TODO add your handling code here:
        int resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente deletar esse cadastro?");
        if(resposta !=0){
            return;
        }
          String msg;
          msg = clsdados.deletarUsuario(usuarioatual);
          JOptionPane.showMessageDialog(rootPane, msg);
          usuarioatual = 0;
          mostrarRegistro();
           preencherTabela();
    }//GEN-LAST:event_cmddeletarActionPerformed

